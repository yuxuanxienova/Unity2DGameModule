using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class DestroyBehaviour : StateMachineBehaviour
{
    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        UnitWithInventory unit = animator.gameObject.GetComponent<UnitWithInventory>();
        //---------Error   Handle------
        if(unit == null){Debug.Log("Error!!! OnStateEnter Destroy Behaviour: UnitWithInventory not found");return;}
        //----------------------------
        EventHandler.CallBeenDestroyedEvent(unit);
        animator.gameObject.tag = "Dead";
        

       
    }

    // OnStateUpdate is called on each Update frame between OnStateEnter and OnStateExit callbacks
    // override public void OnStateUpdate(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
